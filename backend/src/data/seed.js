// ── Currency formatting utility ──
const currencyConfig = {
  USD: { symbol: "$", locale: "en-US", position: "before" },
  INR: { symbol: "₹", locale: "en-IN", position: "before" },
  EUR: { symbol: "€", locale: "en-DE", position: "before" },
  GBP: { symbol: "£", locale: "en-GB", position: "before" },
  AED: { symbol: "AED ", locale: "en-AE", position: "before" },
};

function formatPrice(price, currency) {
  var cfg = currencyConfig[currency] || currencyConfig.USD;
  var formatted;

  if (currency === "INR") {
    // Indian numbering: lakhs and crores
    if (price >= 10000000) {
      formatted = (price / 10000000).toFixed(2).replace(/\.?0+$/, "") + " Cr";
    } else if (price >= 100000) {
      formatted = (price / 100000).toFixed(2).replace(/\.?0+$/, "") + " Lakh";
    } else {
      formatted = price.toLocaleString("en-IN");
    }
    return cfg.symbol + formatted;
  }

  formatted = price.toLocaleString(cfg.locale);
  return cfg.position === "before"
    ? cfg.symbol + formatted
    : formatted + " " + cfg.symbol;
}

